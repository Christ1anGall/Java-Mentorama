public class Variavel {
    public static void main(String[] args) {
        Double valor1 = 12d;
        Double valor2 = 8d;

        Double resultado = valor1 + valor2;

        System.out.println(resultado);

        int valor3 = 12;
        Integer valor4 = 8;

        Integer resultado2 = valor3 + valor4;

        System.out.println(resultado2);

    }
}
