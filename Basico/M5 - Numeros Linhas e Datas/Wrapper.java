public class Wrapper {
    public static void main(String[] args) {
        // int
        // byte
        // char
        // boolean

        Integer myInt = Integer.valueOf(42); // Cria uma referência de wrapper para um int com valor 42
        Byte myByte = Byte.valueOf((byte) 123); // Cria uma referência de wrapper para um byte com valor 123
        Character myChar = Character.valueOf('x'); // Cria uma referência de wrapper para um char com valor 'x'
        Boolean myBoolean = Boolean.valueOf(true); // Cria uma referência de wrapper para um boolean com valor true

    }
}
